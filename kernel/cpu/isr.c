isr_t isr_handlers[256];

void isr_handler(struct interrupt_frame* regs) {
	if(regs->isr_number >= 32 && regs->isr_number < 32 + 16){
		pic_send_eoi(regs->isr_number - 32);
	}
	if(isr_handlers[regs->isr_number] != 0){
		isr_handlers[regs->isr_number](regs);
	}
}

void print_regs(struct interrupt_frame* regs) {
    uint64_t cr2;
    asm volatile ("mov %%cr2, %0" : "=r" (cr2));
	printf("RAX: %llx RBX: %llx RCX: %llx RDX: %llx\n", regs->rax, regs->rbx, regs->rcx, regs->rdx);
    printf("RSI: %llx RDI: %llx RBP: %llx R8 : %llx\n", regs->rsi, regs->rdi, regs->rbp, regs->r8);
    printf("R9 : %llx R10: %llx R11: %llx R12: %llx\n", regs->r9, regs->r10, regs->r11, regs->r12);
    printf("R13: %llx R14: %llx R15: %llx ISR: %llx\n", regs->r13, regs->r14, regs->r15, regs->isr_number);
    printf("ERR: %llx RIP: %llx CS : %llx RFLAGS: %llx\n", regs->err_code, regs->rip, regs->cs, regs->rflags);
    printf("RSP: %llx SS : %llx CR2 : %llx\n", regs->rsp, regs->ss, cr2);
}

void generic_exception_handler(struct interrupt_frame* regs){
	if(current_task != NULL){
		if(current_task->user){
			printf("Exception %d in task %d, ending task\n",regs->isr_number,current_task->id);
			print_regs(regs);
			irq_enable();
			task_exit(1);
			return;
		}
	}

	print_regs(regs);
	panic("CPU Exception %llu\nRIP:%p",regs->isr_number,regs->rip);
}

void register_isr_handler(unsigned char num,void* handler){
	isr_handlers[num] = handler;
}

void register_irq_handler(unsigned char num,void* handler){
	isr_handlers[num + 32] = handler;
}


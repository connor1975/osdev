#include <io.h>

void pic_send_eoi(int irq){
    if(irq > 7){
        outb(0xA0,0x20);
    }
    outb(0x20,0x20);
}

// initialise the pic chip and map irqs to offset 0x20
void pic_init() {
    outb(0x20, 0x11);
    outb(0xA0, 0x11);
    outb(0x21, 0x20);
    outb(0xA1, 0x28);
    outb(0x21, 0x04);
    outb(0xA1, 0x02);
    outb(0x21, 0x01);
    outb(0xA1, 0x01);
    outb(0x21, 0x0);
    outb(0xA1, 0x0);	
}